function normalize(value) {
	return value.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").replace(/[^a-z0-9]+/g, " ").trim();
}
function tokenize(value) {
	const stop = new Set([
		"dhe",
		"per",
		"nga",
		"eshte",
		"jane",
		"cili",
		"cila",
		"cfare",
		"kur"
	]);
	return normalize(value).split(/\s+/).filter((token) => token.length > 2 && !stop.has(token));
}
function retrieveFaq(question, limit = 4) {
	const tokens = tokenize(question);
	return CITIZEN_FAQ.map((item) => {
		const haystack = normalize(`${item.category} ${item.question} ${item.answer} ${item.tags.join(" ")}`);
		return {
			item,
			score: tokens.reduce((total, token) => total + (haystack.includes(token) ? 1 : 0), 0)
		};
	}).filter((entry) => entry.score > 0).sort((a, b) => b.score - a.score).slice(0, limit).map((entry) => entry.item);
}
function fallbackFaqAnswer(question) {
	const matches = retrieveFaq(question, 3);
	if (!matches.length) return {
		answer: "Nuk gjeta nje pergjigje te sakte te pyetjet e shpeshta. Provoni te pyesni per gjurmimin, dokumentet, proceduren e pershpejtuar, ankesen, EKB ose shpronesimin.",
		citations: [],
		hasEnoughInfo: false
	};
	return {
		answer: matches.map((item) => item.answer).join("\n\n"),
		citations: matches.map((item) => ({
			id: item.id,
			title: item.question,
			source: item.category
		})),
		hasEnoughInfo: true
	};
}
function renderFaqForPrompt(items = CITIZEN_FAQ) {
	return items.map((item, index) => `[#${index + 1} | id=${item.id}] ${item.category} - ${item.question}\n${item.answer}`).join("\n\n");
}
